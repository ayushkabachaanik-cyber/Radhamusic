# ================= ADMIN CHECK =================

async def is_admin(chat_id, user_id):
    async for admin in app.get_chat_members(chat_id, filter=ChatMembersFilter.ADMINISTRATORS):
        if admin.user.id == user_id:
            return True
    return False


# ================= CORE TAG FUNCTION =================

async def tag_all(client, message, text_list=None, custom_text=None):
    chat_id = message.chat.id
    tagging_chats[chat_id] = True

    users = []
    async for member in client.get_chat_members(chat_id):
        if member.user and not member.user.is_bot:
            users.append(member.user)

    for i in range(0, len(users), 5):
        if not tagging_chats.get(chat_id):
            break

        chunk = users[i:i+5]
        mentions = ""

        for user in chunk:
            name = user.first_name
            mentions += f"[{name}](tg://user?id={user.id}) "

        # SELECT MESSAGE
        if custom_text:
            msg = custom_text
        elif text_list:
            msg = random.choice(text_list)
        else:
            msg = ""

        await message.reply_text(
            f"{mentions}\n\n{msg}",
            parse_mode=ParseMode.MARKDOWN
        )

        await asyncio.sleep(2)


# ================= STOP FUNCTION =================

async def stop_tag(chat_id):
    tagging_chats[chat_id] = False


