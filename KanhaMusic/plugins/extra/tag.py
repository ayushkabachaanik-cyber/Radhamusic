# 🌅 GM TAG
@app.on_message(filters.command("gmtag") & filters.group)
async def gmtag(_, message: Message):
    if not await is_admin(message.chat.id, message.from_user.id):
        return await message.reply("❌ Admin only")

    text = " ".join(message.command[1:]) if len(message.command) > 1 else None
    await tag_all(app, message, GM_MSG, text)


@app.on_message(filters.command("gmstop") & filters.group)
async def gmstop(_, message: Message):
    await stop_tag(message.chat.id)
    await message.reply("🛑 GM Tag stopped")


